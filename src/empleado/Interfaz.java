    private void jButtonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirActionPerformed
        jButtonBorrar.setEnabled(false);

        if (jRadioButtonJP.isSelected() == true) {
            em5 = new JefesDeProyectos(jTextFieldNombre.getText() + " ", jTextFieldApell.getText(), Double.parseDouble(jTextFieldSalBase.getText()), Integer.parseInt(jTextFieldComision.getText()));
        }

        if (jRadioButtonTH.isSelected() == true) {
            em5 = new TrabajadoresPorHoras(jTextFieldNombre.getText() + " ", jTextFieldApell.getText(), Integer.parseInt(jTextFieldSalBase.getText()), Integer.parseInt(jTextFieldComision.getText()));
        }

        if (jRadioButtonMontador.isSelected() == true) {
            em5 = new Montador(jTextFieldNombre.getText() + " ", jTextFieldApell.getText(), Integer.parseInt(jTextFieldSalBase.getText()), Integer.parseInt(jTextFieldComision.getText()));
        }

        if (jRadioButtoncomision.isSelected() == true) {
            em5 = new Vendedores(jTextFieldNombre.getText() + " ", jTextFieldApell.getText(), Integer.parseInt(jTextFieldSalBase.getText()), Double.parseDouble(jTextFieldComision.getText()), Double.parseDouble(jTextFieldElec.getText()));
        }

        jTextFieldNombre.setText(null);
        jTextFieldApell.setText(null);
        jTextFieldSalBase.setText(null);
        jTextFieldComision.setText(null);
        jTextFieldElec.setText(null);
        jButtonAñadir.setEnabled(false);
    }//GEN-LAST:event_jButtonAñadirActionPerformed

