    private void jListempleadosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListempleadosValueChanged

        java.util.List l = jListempleados.getSelectedValuesList();
        if (flag == false) {
            if (l.size() == 1) {
                em5 = (Empleado) Empleado.modeloLista.get(jListempleados.getSelectedIndex());
                jTextFieldSalario.setText(Double.toString(em5.salario()));
                jButtonEliminarTrabajador.setEnabled(true);
                jMenuItemBorrarSeleccionado.setEnabled(true);
                jMenuItemBorrSeleccion.setEnabled(false);

            } else {
                jButtonEliminarTrabajador.setEnabled(false);
                jTextFieldSalario.setText(null);
            }

            if (l.size() > 1) {
                jMenuItemBorrarSeleccionado.setEnabled(false);
                jMenuItemBorrSeleccion.setEnabled(true);
            }
        } else {
            flag = false;
        }


    }//GEN-LAST:event_jListempleadosValueChanged

