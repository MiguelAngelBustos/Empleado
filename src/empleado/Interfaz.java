    public void compr() {
        jButtonBorrar.setEnabled(true);

        if (jTextFieldElec.isVisible()) {
            if (jTextFieldNombre.getText().matches("[a-zA-Z ]+")
                    && jTextFieldApell.getText().matches("[a-zA-Z ]+")
                    && jTextFieldComision.getText().matches("[0-9.]+")
                    && jTextFieldSalBase.getText().matches("[0-9.]+")
                    && jTextFieldElec.getText().matches("[0-9.]+")) {
                jButtonAñadir.setEnabled(true);
            } else {
                jButtonAñadir.setEnabled(false);
            }

        } else {
            jButtonBorrar.setEnabled(true);
            if (jTextFieldNombre.getText().matches("[a-zA-Z ]+")
                    && jTextFieldApell.getText().matches("[a-zA-Z ]+")
                    && jTextFieldComision.getText().matches("[0-9.]+")
                    && jTextFieldSalBase.getText().matches("[0-9.]+")) {

                jButtonAñadir.setEnabled(true);

            } else {
                jButtonBorrar.setEnabled(true);

            }
        }

        if (jTextFieldNombre.getText().length() != 0) {
            if (jTextFieldNombre.getText().matches("[a-zA-Z ]+")) {
                jTextFieldNombre.setBackground(Color.WHITE);
            } else {
                jTextFieldNombre.setBackground(Color.RED);
            }
        } else {
            jTextFieldNombre.setBackground(Color.WHITE);
        }

        if (jTextFieldApell.getText().length() != 0) {
            if (jTextFieldApell.getText().matches("[a-zA-Z ]+")) {
                jTextFieldApell.setBackground(Color.WHITE);
            } else {
                jTextFieldApell.setBackground(Color.RED);
            }
        } else {
            jTextFieldApell.setBackground(Color.WHITE);
        }

        if (jTextFieldComision.getText().length() != 0) {
            if (jTextFieldComision.getText().matches("[0-9.]+")) {
                jTextFieldComision.setBackground(Color.WHITE);
            } else {
                jTextFieldComision.setBackground(Color.RED);
            }
        } else {
            jTextFieldComision.setBackground(Color.WHITE);
        }

        if (jTextFieldSalBase.getText().length() != 0) {
            if (jTextFieldSalBase.getText().matches("[0-9.]+")) {
                jTextFieldSalBase.setBackground(Color.WHITE);
            } else {
                jTextFieldSalBase.setBackground(Color.RED);
            }
        } else {
            jTextFieldSalBase.setBackground(Color.WHITE);
        }

        if (jTextFieldElec.getText().length() != 0) {
            if (jTextFieldElec.getText().matches("[0-9.]+")) {
                jTextFieldElec.setBackground(Color.WHITE);
            } else {
                jTextFieldElec.setBackground(Color.RED);
            }
        } else {
            jTextFieldElec.setBackground(Color.WHITE);
        }

    }

    public void ocultar() {

        int opc = 0;
        if (jRadioButtonJP.isSelected()) {
            opc = 1;
        }
        if (jRadioButtonTH.isSelected()) {
            opc = 2;
        }
        if (jRadioButtonMontador.isSelected()) {
            opc = 3;
        }
        if (jRadioButtoncomision.isSelected()) {
            opc = 4;
        }

        switch (opc) {
            case 1:
                jLabelNombre.setVisible(true);
                jLabelApell.setVisible(true);
                jLabelSalBase.setVisible(true);
                jLabelComision.setVisible(true);
                jLabelElec.setVisible(false);

                jTextFieldNombre.setVisible(true);
                jTextFieldApell.setVisible(true);
                jTextFieldSalBase.setVisible(true);
                jTextFieldComision.setVisible(true);
                jTextFieldElec.setVisible(false);

                jLabelSalBase.setText("Salario Base");
                jLabelComision.setText("Nº Proyectos");

                break;

            case 2:
                jLabelNombre.setVisible(true);
                jLabelApell.setVisible(true);
                jLabelSalBase.setVisible(true);
                jLabelComision.setVisible(true);
                jLabelElec.setVisible(false);

                jTextFieldNombre.setVisible(true);
                jTextFieldApell.setVisible(true);
                jTextFieldSalBase.setVisible(true);
                jTextFieldComision.setVisible(true);
                jTextFieldElec.setVisible(false);

                jLabelSalBase.setText("Nº Unidades Montadas");
                jLabelComision.setText("Salario por Unidad Montada");

                break;

            case 3:
                jLabelNombre.setVisible(true);
                jLabelApell.setVisible(true);
                jLabelSalBase.setVisible(true);
                jLabelComision.setVisible(true);
                jLabelElec.setVisible(false);

                jTextFieldNombre.setVisible(true);
                jTextFieldApell.setVisible(true);
                jTextFieldSalBase.setVisible(true);
                jTextFieldComision.setVisible(true);
                jTextFieldElec.setVisible(false);

                jLabelSalBase.setText("Nº Unidades Montadas");
                jLabelComision.setText("Salario por Unidad Montada");
                break;

            case 4:
                jLabelNombre.setVisible(true);
                jLabelApell.setVisible(true);
                jLabelSalBase.setVisible(true);
                jLabelComision.setVisible(true);
                jLabelElec.setVisible(true);

                jTextFieldNombre.setVisible(true);
                jTextFieldApell.setVisible(true);
                jTextFieldSalBase.setVisible(true);
                jTextFieldComision.setVisible(true);
                jTextFieldElec.setVisible(true);

                jLabelSalBase.setText("Salario Base");
                jLabelComision.setText("Salario por Comision");

                break;

        }

    }

